package Com.destination.jsf.day18;

public class pattern1 {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			System.out.println("#");
			
		}
		System.out.println("+++++++++++++++++++++++++++++");
		int j=1;
		while(j<=5) {
			System.out.println("#");
			j++;
			
		}
		System.out.println("+++++++++++++++++++++++++++++");
		int k=1;
		do {
			System.out.println("#");
			
			k++;
		}while(k<=5);
		System.out.println("+++++++++++++++++++++++++++++");
	}

}
